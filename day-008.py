name = input("Enter your name: ")
favourite = input("What is your favourite thing?")
day = input("What day is it today? ")
if day == "monday" or day == "Monday":
  print("I know you can do it", name, "!")
elif day == "tuesday" or day == "Tuesday":
  print("You are doing amazing.")
elif day == "wednesday" or day == "Wednesday":
  print("If you are feeling down just think about", favourite, ".")
elif day == "thursday" or day == "Thursday":
  print("Believe in yourself. ")
elif day == "friday" or day == "Friday":
  print("LET'S GO! ")
elif day == "saturday" or day == "Saturday":
  print("Yay it is the weekend! ")
elif day == "sunday" or day == "Sunday":
  print(name, "enjoy yourself! ")
else:
  print("That's not a day silly!")
